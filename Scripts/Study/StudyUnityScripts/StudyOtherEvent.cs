    // OnCollisionExit2D  物理的な接触が終了したタイミング
    // OnTriggerEnter2D   トリガーに接触したタイミング
    // OnTriggerStay2D    トリガーに接触している間のタイミングに呼ばれ続ける
    // OnTriggerExit2D    トリガーな接触が終了したタイミング
    // OnBecameVisible    ゲームオブジェクトがカメラに写ったタイミング
    // OnBecameInvisible  ゲームオブジェクトがカメラから出て行ったタイミング
    // OnMouseEnter       マウスがゲームオブジェクトに触れたタイミング
    // OnMouseOver        マウスがゲームオブジェクトに触れているタイミングに呼ばれ続ける
    // OnMouseExit        マウスがゲームオブジェクトから離れたタイミング
    // OnMouseDown        ゲームオブジェクトの上でクリックされたタイミング
    // OnMouseDrag        ゲームオブジェクトがドラックされているタイミングに呼ばれ続ける
    // OnMouseUp          ゲームオブジェクトの上でクリックが終了したタイミング

